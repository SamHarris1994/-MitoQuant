function [Hf]= HaarFeature(I,H)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%HaarFeature
%   INPUTS
%   I   : 
%   H   : 
%
%   OUTPUTS
%   Hf  : 
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%   Created By Yang Li ; Date 2016.01.22
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
Hw=(H>0);
Hb=H-Hw;
k=size(find(Hw(:)));
Hw=Hw/k(1);
k=size(find(-Hb(:)));
Hb=Hb/k(1);
H=Hw+Hb;

Hf=convn(I,H,'same');